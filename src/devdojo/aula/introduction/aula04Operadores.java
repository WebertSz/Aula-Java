package devdojo.aula.introduction;

public class aula04Operadores {
    public static void main(String[] args) {

        int numero01 = 10;
        int numero02 = 20;
        int resultado = numero02 - numero01;
        System.out.println(resultado);
        resultado = numero02 + numero01;
        System.out.println(resultado);
        resultado = numero02 * numero01;
        System.out.println(resultado);
        resultado = numero02 / numero01;
        System.out.println(resultado);
    }
}
